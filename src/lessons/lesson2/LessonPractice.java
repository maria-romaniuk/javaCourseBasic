package lessons.lesson2;

public class LessonPractice {
    public static void main(String[] args) {
        int x = 15;
        int y = 22;
        System.out.println("Результат умножения: " + x * y);
        System.out.println();


        byte xByte = 23;
        byte yByte = 18;
        System.out.println("Результат деления: " + xByte / yByte);
        System.out.println("Результат остатка деления: " + xByte % yByte);
        System.out.println();

        double xDouble = 765.5;
        double yDouble = 145.2;
        double zDouble = 2;

        System.out.println("Результат более сложной формулы: " + (xDouble-yDouble)/zDouble);
        System.out.println();

    }
}
