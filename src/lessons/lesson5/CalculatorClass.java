package lessons.lesson5;

public class CalculatorClass {
    public int sum(int a, int b){
        int sumResult = a + b;
        return sumResult;
    }

    public int division(int a, int b){
        return a / b;
    }

    public int subtrac(int x, int y){
        return x - y;
    }

    public int multipl(int x, int y){
        return x * y;
    }

}
